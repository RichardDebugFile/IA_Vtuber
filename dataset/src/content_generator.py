    @staticmethod
    def generate_dataset(num_clips: int) -> List[dict]:
        """
        Generate dataset with unique natural text entries.
        Returns only naturally unique texts, all with sufficient length (6-20 words).

        Args:
            num_clips: Number of clips to generate

        Returns:
            List of dictionaries with id, text, and filename
        """
        dataset = []
        all_texts = []

        # Collect all texts from all categories
        for category, texts in ContentGenerator.TEMPLATES.items():
            all_texts.extend(texts)

        # Shuffle to randomize
        random.shuffle(all_texts)

        # Ensure we have enough texts
        if len(all_texts) < num_clips:
            # If not enough, repeat the list
            repeats_needed = (num_clips // len(all_texts)) + 1
            all_texts = all_texts * repeats_needed
            random.shuffle(all_texts)

        # Generate dataset
        for i in range(num_clips):
            dataset.append({
                "id": i + 1,
                "text": all_texts[i],
                "filename": f"casiopy_{i+1:04d}"
            })

        return dataset

    @staticmethod
    def get_text_stats(dataset: List[dict]) -> dict:
        """
        Get statistics about the generated text dataset.

        Args:
            dataset: List of dataset entries

        Returns:
            Dictionary with statistics
        """
        texts = [entry["text"] for entry in dataset]
        unique_texts = len(set(texts))
        total = len(texts)

        lengths = [len(text) for text in texts]
        avg_length = sum(lengths) / len(lengths) if lengths else 0

        word_counts = [len(text.split()) for text in texts]
        avg_words = sum(word_counts) / len(word_counts) if word_counts else 0

        return {
            "total_entries": total,
            "unique_texts": unique_texts,
            "duplicates": total - unique_texts,
            "avg_text_length_chars": round(avg_length, 1),
            "avg_word_count": round(avg_words, 1),
            "min_length": min(lengths) if lengths else 0,
            "max_length": max(lengths) if lengths else 0,
            "min_words": min(word_counts) if word_counts else 0,
            "max_words": max(word_counts) if word_counts else 0
        }
